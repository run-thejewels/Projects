int	ft_sqrt(int nb)
{
	int i;

	i = 3;
	if (nb == 1)
		return (1);
	else if (nb == 4)
		return (2);
	else
		while (i < (nb / 2))
		{
			if ((i * i) == nb)
				return (i);
			i++;
		}
	return (0);
}
