int	do_op(int a1, int a2, char oper)
{
	if (oper == '+')
		return (a1 + a2);
	if (oper == '-')
		return (a1 - a2);
	if (oper == '*')
		return (a1 * a2);
	if (oper == '/')
		return (a1 / a2);
	if (oper == '%')
		return (a1 % a2);
	return (0);
}
