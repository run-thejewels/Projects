#include <stdlib.h>

int		ft_strchrint(const char *str, int ch)
{
	char	*s;
	int		i;

	i = 0;
	s = (char*)str;
	while (s[i] != ch)
	{
		if (s[i] == '\0')
			return (0);
		i++;
	}
	return (i);
}
