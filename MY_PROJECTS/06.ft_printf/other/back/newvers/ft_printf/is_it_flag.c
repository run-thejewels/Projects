#include "head.h"

int	is_it_flag(char *str1)
{
	int i;
	char		str;

	i = 0;
	while (*str1 == ' ' || *str1 == '0' || *str1 == '-' || *str1 == '+' ||
	*str1 == '#' || *str1 == 'l' || *str1 == 'L' || *str1 == 'h' || *str1 == '.' ||
	(*str1 >= '0' && *str1 <= '9'))
				i++;
	str = str1[i];
	if (str == 'c' || str == 'f' || str == 'd' || str == 's' || str == 'i' ||
		str == 'o' || str == 'u' || str == 'x' || str == 'X' || str == 'p' ||
		str == 'S')
			return (i);
	return (-i);
}
