#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*a;
	size_t	i;

	i = 0;
	if (!(a = (char *)malloc(sizeof(char) * (ft_strlen(str) + 1))))
		return (NULL);
	while (str[i] != '\0')
	{
		a[i] = str[i];
		i++;
	}
	a[i] = '\0';
	return (a);
}
