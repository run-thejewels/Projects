#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*f;

	f = (char *)s;
	while (*f != '\0')
	{
		if (*f == (char)c)
			return (f);
		f++;
	}
	if (c == '\0')
		return (f);
	return (NULL);
}
