#include "libft.h"

char	*ft_strcpy(char *s_out, const char *s_in)
{
	int	i;

	i = 0;
	while (s_in[i])
	{
		s_out[i] = s_in[i];
		i++;
	}
	s_out[i] = s_in[i];
	return (s_out);
}
