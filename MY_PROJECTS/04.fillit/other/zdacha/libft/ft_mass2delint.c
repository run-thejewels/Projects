#include "includes/libft.h"

void	ft_mass2delint(int ***a)
{
	int i;

	i = 0;
	while ((*a)[i] != NULL)
	{
		free((*a)[i]);
		(*a)[i] = NULL;
		i++;
	}
	free(*a);
	*a = NULL;
}
