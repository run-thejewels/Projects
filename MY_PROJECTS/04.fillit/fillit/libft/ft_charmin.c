#include "includes/libft.h"

int		ft_charmin(char *a, int n)
{
	int	min;
	int i;

	i = 0;
	min = *a;
	while (i < n)
	{
		if (min > a[i] - '0')
			min = a[i] - '0';
		i++;
	}
	return (min);
}
