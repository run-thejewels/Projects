#include "libft.h"

int		ft_sqr_int(int nbr, size_t rank)
{
	int		dst;

	dst = 1;
	if (rank == 0)
		return (1);
	while (rank > 0)
	{
		dst *= nbr;
		rank--;
	}
	return (dst);
}
